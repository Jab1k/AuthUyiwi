getimage(String name) {
  if (name.toLowerCase() == 'gta 5 rp') {
    return 'https://staticg.sportskeeda.com/editor/2022/04/8790f-16508069952621-1920.jpg';
  } else if (name.toLowerCase() == 'rust') {
    return 'https://image.api.playstation.com/vulcan/ap/rnd/202103/1609/iXyUOZpAfq34g4CGsgUb9VEc.png';
  } else if (name.toLowerCase() == 'raft') {
    return 'https://cdn.akamai.steamstatic.com/steam/apps/648800/capsule_616x353.jpg?t=1655744208';
  } else if (name.toLowerCase() == 'csgo') {
    return 'https://cdn.akamai.steamstatic.com/steam/apps/730/capsule_616x353.jpg?t=1668125812';
  }
}
